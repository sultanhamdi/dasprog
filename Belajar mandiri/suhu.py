farenheit = float(input("masukkan suhu dalam farenheit: "))
print(f"suhu adalah: {farenheit} farenheit ")

#Celcius
celcius = (5/9)*(farenheit-32)
print(f"suhu adalah: {celcius} celcius ")

#Reamur
reamur = (4/9)*(farenheit-32)
print(f"suhu adalah: {reamur} reamur ")

#kelvin
kelvin = (5/9)*(farenheit-32)+273
print(f"suhu adalah: {kelvin} kelvin ")